package com.pojo;

public class Eye {

	private int eyesid;
	private String eyesname;
	private String eyesname1;
	private String eyespic;
	public int getEyesid() {
		return eyesid;
	}
	public void setEyesid(int eyesid) {
		this.eyesid = eyesid;
	}
	public String getEyesname() {
		return eyesname;
	}
	public void setEyesname(String eyesname) {
		this.eyesname = eyesname;
	}
	public String getEyesname1() {
		return eyesname1;
	}
	public void setEyesname1(String eyesname1) {
		this.eyesname1 = eyesname1;
	}
	public String getEyespic() {
		return eyespic;
	}
	public void setEyespic(String eyespic) {
		this.eyespic = eyespic;
	}
	public String getEyesprice() {
		return eyesprice;
	}
	public void setEyesprice(String eyesprice) {
		this.eyesprice = eyesprice;
	}
	private String eyesprice;
}
